#  Checking Multiple Conditions-
# >Something you need more than one condition to be true, in those time the keywords (and) and (or) can help you in these conditions.

#1. Using 'and' to check multiple conditions-
#  >To check multiple conditions simply use the keyword 'and' to combine required conditional tests.

#2. Using 'or' to check multiple conditions-
#  >The keyword 'or' also checks the multiple conditions, but it passes when either or both of the individual tests fail.

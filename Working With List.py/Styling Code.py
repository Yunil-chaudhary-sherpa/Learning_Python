#  Styling code-
# >As there has been a lot of progress in learning python, there are going to be longer codes you have to perform. While creating longer programs we need to style the code. Programmers have agreed on a number of syling conventions to ensure that everyone's code is structured as same as possible.

#1. Styling Guide-
#  >The python style guide was written with the understanding that code is read more than written. So the prgrammers were asked to write a code that is easy to read rather than easy to write.

#2. Indentation-
#  >PEP 8 recommends that you use four spaces per indentation level. 

#3. Line Length-
#  >Many programmers recommend that each line should be less than 80 characters whereas comments should be less than 72 characters. Though, PEP 8 guidlines for line length to not be greater than 99 characters.

#4. Blank Lines-
#  >To group parts of programs visually, blank lines are used. Use the blank lines at the right moment to seperate the parts of program with different tasks.

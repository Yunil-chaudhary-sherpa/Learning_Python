#  Converting the case of the word 

#1. title()- Displays each word in titlecase (Uppercases the first letter of word)
name = 'ada lovelace'
print(name.title())

#2. upper()- Displays each word in uppercase
name = 'ada loVelAce'
print(name.upper())

#3. lower()- Displays each word in lowercase
name = 'Ada LoVelAce'
print(name.lower())

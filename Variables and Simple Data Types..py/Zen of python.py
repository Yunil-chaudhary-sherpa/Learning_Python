# Zen of Python

#1. Beautiful is better than ugly-
# > In t python programmers have always preferred  well-designed, efficient, and beautiful solutionsto problems.

#2. Simple is better than complex-
# > If you have to choose between simple and complex solution and both solutions worked, use the simple solution

#3. Complex is better than complicated-
# > A task usually never has a simple solution. So, even if the solutions are complex always the simplest solution.

#4. Readability counts-
# > Though the program is complex to understand make the program as readable as possible.

#5. There should be one-- and preferably only one-- obvious way to do it-
# > If two programmers are writing a code though a problem will have many way to get a solution, finding the simplest solution leads to similar solutions. So always try to find a solution that has a common solution.

#6. Now is better than never-
# > A perso can never fully master a programming language. Thus, dont try to make a perfect code; write a code that works and when it needs improvement add new things that are required.


# While creating a program arranging the order inside a particular list becomes hectic.

#1. Sort Method-
#  >Python's sort() method makes it relatively easy to sort a list as it sorts the values in a list arranged in alphabatical order.
Siblings_list = ['dipak','tendy','dendy','pasang','sonam','leezon','khuili']
Siblings_list.sort()
print(Siblings_list)

# The values can also be sorted in reverse alphabetic order.
Siblings_list.sort(reverse=True)
print(Siblings_list)

#2. Sorted Method-
#  >For maintaing the order of the list printing it in a sorted order this method is used.
Siblings_list = ['dipak','tendy','dendy','pasang','sonam','leezon','khuili']
print(Siblings_list)
print(sorted(Siblings_list))
print(Siblings_list)
